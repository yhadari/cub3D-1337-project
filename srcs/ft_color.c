#include "cub.h"

int		ft_color(int n)
{
	int nb;
	int i;

	nb = 0;
	i = 0;
	nb += n == 1 ? g_gt.f[i++] * 65536 : g_gt.c[i++] * 65536;
	nb += n == 1 ? g_gt.f[i++] * 256 : g_gt.c[i++] * 256;
	nb += n == 1 ? g_gt.f[i] : g_gt.c[i];
	return (nb);
}
